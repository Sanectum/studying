import * as dotenv from "dotenv";

// Загружаем переменные окружения из файла .env.
// Это позволяет хранить конфигурацию отдельно от кода,
// не хардкодить URL‑ы и ключи прямо в проекте.
dotenv.config();

/**
 * Объект env — единая точка доступа к переменным окружения.
 *
 * Такой подход:
 *   - упрощает работу с конфигурацией
 *   - делает код чище (нет process.env по всему проекту)
 *   - позволяет централизованно валидировать переменные
 *
 * Восклицательный знак (!) говорит TypeScript:
 *   "я гарантирую, что переменная существует".
 * Если переменной нет — приложение упадёт при старте,
 * что лучше, чем тихая ошибка в тестах.
 */
export const env = {
  todoApi: process.env.TODO_API_URL!,
};
